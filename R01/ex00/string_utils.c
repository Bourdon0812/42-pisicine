#include <unistd.h>

////////////////////////////////////////////////////////////////////////////
// return len of str
unsigned int	ft_strlen(char *str)
{
	unsigned int	i;

	i = 0;
	while (str[i] != '\0')
		i++;
	return (i);
}

////////////////////////////////////////////////////////////////////////////
// write a str with len(str) size
void	ft_putstr(char *str)
{
	write(1, str, ft_strlen(str));
}
