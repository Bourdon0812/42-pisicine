#include <unistd.h>
#include <stdio.h>

int	ft_is_divisible(int nb, int div)
{
	if (div * div > nb)
		return (0);
	if (nb % div == 0)
		return (1);
	return (ft_is_divisible(nb, div + 1));
}

int	ft_is_prime(int nb)
{
	if (nb <= 1)
		return (0);
	return (!ft_is_divisible(nb, 2));
}

int	ft_find_next_prime(int nb)
{
	if (nb <= 2)
		return (2);
	while (!ft_is_prime(nb))
		nb++;
	return (nb);
}
